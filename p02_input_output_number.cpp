/* 
Q2. Write a Program to Read and Print Number Input From the User.
For example: 
Input: Enter any Number: 25
Output: Entered Number: 25
*/

# include <iostream>

int main() {
	double n;
	std :: cout << "Enter any number: ";
	std :: cin >> n;
	std :: cout << "Entered number: " << n;
	return 0;
}
